function parsePuzzle(puzzleData) {
    var puzzleString = puzzleData.split(' ')[1];
    var puzzle = [];

    for (var i = 0; i < puzzleString.length; i += 9) {
        puzzle.push(puzzleString.slice(i, i + 9).split('').map(char => (char === '0') ? '' : char));
    }

    return puzzle;
}

function initializeDigits() {
    // Digits 1-9 (only if not initialized)
    if (!isTilesInitialized) {
        for (var i = 1; i <= 9; i++) {
            var img = document.createElement("img");
            img.src = getImageForValue(i);
            img.alt = "Cat " + i;
            img.id = i;
            img.addEventListener("click", selectNumber);
            img.classList.add("number");
            document.getElementById("digits").appendChild(img);
        }

        // Set the flag to true after initializing the tiles
        isTilesInitialized = true;
    }
}



function setRandomGame() {
    document.body.style.backgroundImage = "url('catGif.gif')"; // Replace 'catGif.gif' with the path of the original GIF
    var randomIndex = Math.floor(Math.random() * puzzles.length);
    currentPuzzle = puzzles[randomIndex];

    // Generate a new index until it's different from the last one
    do {
        messageIndex = Math.floor(Math.random() * textOutputs.length);
    } while (messageIndex === lastMessageIndex);

    // Update the last used index
    lastMessageIndex = messageIndex;

    // Reset the flag when a new puzzle is set
    isTilesInitialized = false;

    setGame();
}

function setGame() {
    // Clear previous board
    document.getElementById("board").innerHTML = "";

    if (currentPuzzle) {
        for (var r = 0; r < 9; r++) {
            for (var c = 0; c < 9; c++) {
                var tile = document.createElement("div");
                tile.id = r.toString() + "-" + c.toString();

                if (currentPuzzle[r][c] !== "") {
                    var value = parseInt(currentPuzzle[r][c]); // Convert to a number
                    var img = document.createElement("img");
                    img.src = getImageForValue(value);
                    img.alt = value;

                    // Set width and height attributes for scaling down
                    img.width = 38;
                    img.height = 38;

                    tile.appendChild(img);

                    if (currentPuzzle[r][c] != "") {
                        tile.classList.add("tile-start");
                    }
                }

                if (r == 2 || r == 5) {
                    tile.classList.add("horizontal-line");
                }
                if (c == 2 || c == 5) {
                    tile.classList.add("vertical-line");
                }
                tile.addEventListener("click", selectTile);
                tile.classList.add("tile");
                document.getElementById("board").appendChild(tile);
            }
        }
    }
}


